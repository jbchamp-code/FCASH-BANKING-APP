    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        DefaultTableModel tableModel = (DefaultTableModel) tblTransaction.getModel();
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(tableModel);
        tblTransaction.setRowSorter(sorter);
        if (txtSearchbar.getText().trim().equals("")) {
            sorter.setRowFilter(null);
        } else {
            int targetColumn = cbCol_To_Search.getSelectedIndex(); 
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + txtSearchbar.getText(), targetColumn)); 
        }
        
    }//GEN-LAST:event_btnSearchMouseClicked

