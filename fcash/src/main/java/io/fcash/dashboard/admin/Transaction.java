    private void loadData() {
        String query = "SELECT \n" +
            "acc.id AS id, \n" +
            "profile.firstname AS firstname, \n" +
            "profile.middlename AS middlename, \n" +
            "profile.lastname AS lastname, \n" +
            "profile.suffix AS suffix, \n" +
            "accTransact.amount AS amount,\n" +
            "accTransact.transactionDate AS transactionDate,\n" +
            "accTransact.transactionType AS transactionType\n" +
            "FROM `tblaccount` AS acc\n" +
            "INNER JOIN `tblprofile` AS profile \n" +
            "ON acc.id = profile.profileID\n" +
            "INNER JOIN `tbltransaction` AS accTransact \n" +
            "ON acc.id = accTransact.costumerID;";
        
        DefaultTableModel model = new DefaultTableModel(new String[]{"USERID", "FULL NAME", "AMOUNT", "TRANSACTION TYPE", "DATE"}, 0);
        Connection con = null;
        try {
            con = DriverManager.getConnection(Database.dbURL, Database.dbUsername, Database.dbPassword);
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            
            
            while (rs.next()) {
                int id = rs.getInt("id");
                String fullname = rs.getString("lastname")+" "+rs.getString("suffix")+", "+rs.getString("firstname")+" "+rs.getString("middlename");
                double amount = rs.getDouble("amount");
                String type = rs.getString("transactionType").toUpperCase();
                
                LocalDateTime transactionDate = rs.getObject("transactionDate", LocalDateTime.class);
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("MMMM dd, yyyy hh:mm a");
                String formattedDate; 
                formattedDate = transactionDate.format(formatter);
                
                // Add row to the model
                model.addRow(new Object[]{id, fullname, amount, type, formattedDate});
                
            }
            
            tblTransaction.setModel(model);
        
        
        } catch (SQLException ex) {
            System.out.println(ex);
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException closeEx) {
                    System.out.println(closeEx);
                }
            }
        }
        
    }
