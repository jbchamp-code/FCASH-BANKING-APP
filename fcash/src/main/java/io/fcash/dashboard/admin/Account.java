    private void changeAccountStatus(int id, String status) {
        String update_query = "UPDATE `tblsavingsaccount` SET `accountStatus`= ? WHERE `id`= ?;";
        Connection con = null;
            try {
                    con = DriverManager.getConnection(Database.dbURL, Database.dbUsername, Database.dbPassword);
                    PreparedStatement pstmt = con.prepareStatement(update_query);
                    pstmt.setString(1, status);
                    pstmt.setInt(2, id);
                    pstmt.executeUpdate();
                    JOptionPane.showMessageDialog(null, 
                "Account Status Updated.", 
                "Account.", 
                JOptionPane.INFORMATION_MESSAGE);
                    refreshTableData();
                } catch (SQLException ex) {
                    if (con != null) {
                        try {
                            con.rollback();
                        } catch (SQLException rollbackEx) {
                            System.out.println(rollbackEx);
                        }
                    }
                } finally {
                    if (con != null) {
                        try {
                            con.close();
                        } catch (SQLException closeEx) {
                            System.out.println(closeEx);
                        }
                    }
                }
    
    
    }
