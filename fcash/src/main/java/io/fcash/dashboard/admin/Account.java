    private void btnActivateAccountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActivateAccountMouseClicked
        int result = JOptionPane.showConfirmDialog(
            null,
            "Do you want to save changes?", 
            "Confirm Action",
            JOptionPane.YES_NO_OPTION
        );
        String query = "UPDATE `tblsavingsaccount` SET accountStatus = ?, openingDate = NOW() WHERE id = ?";
        
        // Process the result
        if (result == JOptionPane.YES_OPTION) {
            
            try {
                Connection con = DriverManager.getConnection(Database.dbURL, Database.dbUsername, Database.dbPassword);
                PreparedStatement stmt = con.prepareStatement(query);
                
                //get hidden ID
                int row = tblAccount.getSelectedRow();
                int modelRow = tblAccount.convertRowIndexToModel(row);
                int id = (int)tblAccount.getModel().getValueAt(modelRow, 0);
                
                stmt.setString(1, "Active"); 
                stmt.setInt(2, id);
                
                stmt.executeUpdate();
                
                
            } catch (SQLException ex) {
                System.getLogger(Account.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
        refreshTableData();
    }//GEN-LAST:event_btnActivateAccountMouseClicked

