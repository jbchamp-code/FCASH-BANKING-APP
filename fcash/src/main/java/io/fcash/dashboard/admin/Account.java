    private void btnAccChangeStatusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAccChangeStatusMouseClicked
        String[] choices = { "Active", "Dormant", "Frozen", "Closed"};
        
        String selection = (String) JOptionPane.showInputDialog(
            null,                          
            "Account status change:",    
            "Status Type:",            
            JOptionPane.QUESTION_MESSAGE,  
            null,                          
            choices,                    
            choices[0]                     
        );

        int row = tblAccount.getSelectedRow();
        int modelRow = tblAccount.convertRowIndexToModel(row);
        int id = (int)tblAccount.getModel().getValueAt(modelRow, 0);
                
        if (selection.equalsIgnoreCase("Active")) {
            changeAccountStatus(id, "Active");
        }else if (selection.equalsIgnoreCase("Dormant")) {
            changeAccountStatus(id, "Dormant");
        }else if (selection.equalsIgnoreCase("Frozen")) {
            changeAccountStatus(id, "Frozen");
        }else if (selection.equalsIgnoreCase("Closed")) {
            changeAccountStatus(id, "Closed");
        }
    }//GEN-LAST:event_btnAccChangeStatusMouseClicked

