    private void refreshTableData() {
        String query = "SELECT \n" +
            "acc.id AS id,\n" +
            "profile.firstname AS firstname, \n" +
            "profile.middlename AS middlename, \n" +
            "profile.lastname AS lastname, \n" +
            "profile.suffix AS suffix, \n" +
            "accSave.accountStatus AS status\n" +
            "FROM `tblaccount` AS acc\n" +
            "INNER JOIN `tblprofile` AS profile \n" +
            "ON acc.id = profile.profileID \n" +
            "INNER JOIN `tblsavingsaccount` AS accSave\n" +
            "ON acc.id = accSave.id;";
            
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Full Name", "Account Status"}, 0);
        Connection con = null;
        try {
            con = DriverManager.getConnection(Database.dbURL, Database.dbUsername, Database.dbPassword);
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            
            while (rs.next()) {
                int id = rs.getInt("id");
                String fullname = rs.getString("lastname")+" "+rs.getString("suffix")+", "+rs.getString("firstname")+" "+rs.getString("middlename");
                String status = rs.getString("status");

                // Add row to the model
                model.addRow(new Object[]{id, fullname, status, ""});
                
            }
            
            tblAccount.setModel(model);
            tblAccount.removeColumn(tblAccount.getColumnModel().getColumn(0));
        } catch (SQLException ex) {
            System.out.println(ex);
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException closeEx) {
                    System.out.println(closeEx);
                }
            }
        }
        
    
    }

