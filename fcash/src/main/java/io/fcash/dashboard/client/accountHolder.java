    private void refreshTableData() {
        String query = "SELECT \n" +
            "acc.id AS id, \n" +
            "profile.firstname AS firstname, \n" +
            "profile.middlename AS middlename, \n" +
            "profile.lastname AS lastname, \n" +
            "profile.suffix AS suffix, \n" +
            "accSave.balance AS balance\n" +
            "FROM `tblaccount` AS acc\n" +
            "INNER JOIN `tblprofile` AS profile \n" +
            "ON acc.id = profile.profileID\n" +
            "INNER JOIN `tblsavingsaccount` AS accSave \n" +
            "ON acc.id = accSave.id;";
        
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Full Name", "Balance"}, 0);
        Connection con = null;
        try {
            con = DriverManager.getConnection(Database.dbURL, Database.dbUsername, Database.dbPassword);
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            
            
            while (rs.next()) {
                int id = rs.getInt("id");
                String fullname = rs.getString("lastname")+""+(rs.getString("suffix").trim().equals("")? "" : " "+rs.getString("suffix"))+", "+rs.getString("firstname")+" "+rs.getString("middlename");
                double balance = rs.getDouble("balance");

                // Add row to the model
                model.addRow(new Object[]{id, fullname, balance});
                
            }
            
            tblAccount.setModel(model);
            tblAccount.removeColumn(tblAccount.getColumnModel().getColumn(0));
        
        
        } catch (SQLException ex) {
            System.out.println(ex);
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException closeEx) {
                    System.out.println(closeEx);
                }
            }
        }
        
    }

    private void updateSavingsAccount(String query, int id, double current_balance, double amount, String transactionType) {
        Connection con = null;
        try {
            con = DriverManager.getConnection(Database.dbURL, Database.dbUsername, Database.dbPassword);
            PreparedStatement pstmt = con.prepareStatement(query);
            if(transactionType.equalsIgnoreCase("deposit")){
                pstmt.setDouble(1, (current_balance+amount));
            }else if(transactionType.equalsIgnoreCase("withdraw")){
                pstmt.setDouble(1, (current_balance-amount));
            }
            pstmt.setInt(2, id);
            pstmt.executeUpdate();

        } catch (SQLException ex) {
            System.out.println(ex);
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException closeEx) {
                    System.out.println(closeEx);
                }
            }
        }
    }

    private void insertTransaction(String query, int id, double amount, String transactionType, int clientID) {
        Connection con = null;
        try {
            con = DriverManager.getConnection(Database.dbURL, Database.dbUsername, Database.dbPassword);
            PreparedStatement pstmt = con.prepareStatement(query);
            pstmt.setDouble(1, id);
            pstmt.setDouble(2, amount);
            pstmt.setString(3, transactionType);
            pstmt.setInt(4, clientID);
            
            pstmt.executeUpdate();
            
        } catch (SQLException ex) {
            System.out.println(ex);
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException closeEx) {
                    System.out.println(closeEx);
                }
            }
        }
    
    }

    private Object accountStatus(int id) {
        String status = "";
        String select_query = "SELECT acc.`id`, savAcc.`accountStatus` FROM `tblaccount` AS acc INNER JOIN `tblsavingsaccount` AS savAcc ON acc.id = savAcc.id WHERE acc.`id` = ?;";
            
        Connection con = null;
        ResultSet rs;
        try {
            con = DriverManager.getConnection(Database.dbURL, Database.dbUsername, Database.dbPassword);
            PreparedStatement pstmt = con.prepareStatement(select_query);
            pstmt.setInt(1, id);
            rs = pstmt.executeQuery();
            
            if(rs.next()){
                status = rs.getString("accountStatus");
                
            } 
            
        } catch (SQLException ex) {
            System.out.println(ex);
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException closeEx) {
                    System.out.println(closeEx);
                }
            }
        }
        
        return status;
    }
