    private void btnDepositMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDepositMouseClicked
        
        //get hidden ID
        int row = tblAccount.getSelectedRow();
        int modelRow = tblAccount.convertRowIndexToModel(row);
        int id = (int)tblAccount.getModel().getValueAt(modelRow, 0);
        
        double deposit_amount = -1;
        double current_balance = (double)tblAccount.getModel().getValueAt(modelRow, 2);
        
        boolean isValid = false;
        if(accountStatus(id).equals("Dormant")){
            JOptionPane.showMessageDialog(this, 
                    "Cannot Deposit. The account is Dormant State.", 
                    "Account", 
                    JOptionPane.INFORMATION_MESSAGE);
        }else if(accountStatus(id).equals("Frozen")){
            JOptionPane.showMessageDialog(this, 
                    "Cannot Deposit. The account is Frozen State.", 
                    "Account", 
                    JOptionPane.INFORMATION_MESSAGE);
        }else if(accountStatus(id).equals("Closed")){
            JOptionPane.showMessageDialog(this, 
                    "Cannot Deposit. The account is Closed.", 
                    "Account", 
                    JOptionPane.INFORMATION_MESSAGE);
        }else{
            while (!isValid) {
                String amount = JOptionPane.showInputDialog("Enter the amount to deposit:");

                try {
                    deposit_amount = Double.parseDouble(amount);

                    if(amount == null){
                        break;
                    } else if (deposit_amount > 0) {
                        int result = JOptionPane.showConfirmDialog(
                            null,
                            "Do you want to save changes?", 
                            "Confirm Action",
                            JOptionPane.YES_NO_OPTION
                        );
                        if (result == JOptionPane.YES_OPTION) {

                           String updateSavingsAccount = "UPDATE `tblsavingsaccount` SET `balance` = ? WHERE `tblsavingsaccount`.`id` = ?;";
                           String insertTransaction = "INSERT INTO `tbltransaction` (`costumerID`, `amount`, `transactionType`, `transactionDate`, `IssuedByID`) VALUES (?, ?, ?, current_timestamp(), ?)";

                           updateSavingsAccount(updateSavingsAccount, id, current_balance, deposit_amount, "deposit");
                           insertTransaction(insertTransaction, id, deposit_amount, "deposit", userid);
                           refreshTableData(); 

                           JOptionPane.showMessageDialog(null, "Transaction Complete.");
                           isValid = true;

                        }else{
                           isValid = false;
                        }
                    } else if(deposit_amount == 0){
                        break;
                    } else {
                        JOptionPane.showMessageDialog(null, "Error: Amount cannot be negative.");
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Error: Please enter a valid number.");
                }

            }
        }
        
        
    }//GEN-LAST:event_btnDepositMouseClicked

