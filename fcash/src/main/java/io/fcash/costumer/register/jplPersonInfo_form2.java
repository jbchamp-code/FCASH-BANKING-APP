    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        if(jrbtnGenderMale.isSelected())
            Registration.gender = "Male";
        else if(jrbtnGenderFemale.isSelected())
            Registration.gender = "Female";
        
        if(jrbtnCVSingle.isSelected())
            Registration.civilStatus = "Single";
        else if(jrbtnCVMarried.isSelected())
            Registration.civilStatus = "Married";
        else if(jrbtnCVDivorced.isSelected())
            Registration.civilStatus = "Divorced";
        
        Registration.nationality = txtnationality.getText();
        Registration.placeOfBirth = txtplaceOfBrith.getText();
        
        Registration.residential_Address_HouseNumber = txtResAddr_HouseNumber.getText();
        Registration.residential_Address_Street = txtResAddr_Street.getText();
        Registration.residential_Address_Barangay = txtResAddr_Barangay.getText();
        Registration.residential_Address_Province = txtResAddr_Province.getText();
        Registration.residential_Address_zipcode = txtResAddr_zipcode.getText();
        
        if(cbPermaAddress.isSelected()){
            Registration.permanent_Address_HouseNumber = txtResAddr_HouseNumber.getText();
            Registration.permanent_Address_Street = txtResAddr_Street.getText();
            Registration.permanent_Address_Barangay = txtResAddr_Barangay.getText();
            Registration.permanent_Address_Province = txtResAddr_Province.getText();
            Registration.permanent_Address_zipcode = txtResAddr_zipcode.getText();
            txtPermaAddr_HouseNumber.setText("");
            txtPermaAddr_Street.setText("");
            txtPermaAddr_Barangay.setText("");
            txtPermaAddr_Province.setText("");
            txtPermaAddr_zipcode.setText("");
            txtPermaAddr_HouseNumber.setEnabled(false);
            txtPermaAddr_Street.setEnabled(false);
            txtPermaAddr_Barangay.setEnabled(false);
            txtPermaAddr_Province.setEnabled(false);
            txtPermaAddr_zipcode.setEnabled(false);
        }else{
            Registration.permanent_Address_HouseNumber = txtPermaAddr_HouseNumber.getText();
            Registration.permanent_Address_Street = txtPermaAddr_Street.getText();
            Registration.permanent_Address_Barangay = txtPermaAddr_Barangay.getText();
            Registration.permanent_Address_Province = txtPermaAddr_Province.getText();
            Registration.permanent_Address_zipcode = txtPermaAddr_zipcode.getText();
        
        }
        
        JFrame jfregister = (JFrame) SwingUtilities.getAncestorOfClass(JFrame.class, this);
        jplPersonInfo_Review pInfo = new jplPersonInfo_Review();
        jfregister.remove(this); 
        jfregister.getContentPane().add(pInfo);
        jfregister.revalidate();
        jfregister.repaint();
        
    }//GEN-LAST:event_btnNextActionPerformed

    private void cbPermaAddressStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_cbPermaAddressStateChanged
        if(cbPermaAddress.isSelected()){
            txtPermaAddr_HouseNumber.setText("");
            txtPermaAddr_Street.setText("");
            txtPermaAddr_Barangay.setText("");
            txtPermaAddr_Province.setText("");
            txtPermaAddr_zipcode.setText("");
            txtPermaAddr_HouseNumber.setEnabled(false);
            txtPermaAddr_Street.setEnabled(false);
            txtPermaAddr_Barangay.setEnabled(false);
            txtPermaAddr_Province.setEnabled(false);
            txtPermaAddr_zipcode.setEnabled(false);
        }else{
            txtPermaAddr_HouseNumber.setEnabled(true);
            txtPermaAddr_Street.setEnabled(true);
            txtPermaAddr_Barangay.setEnabled(true);
            txtPermaAddr_Province.setEnabled(true);
            txtPermaAddr_zipcode.setEnabled(true);
        }
    }//GEN-LAST:event_cbPermaAddressStateChanged

