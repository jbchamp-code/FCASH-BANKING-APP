    private void loadFormData() {
        String query = "SELECT \n" +
            "profile.nationality AS nationality, \n" +
            "profile.placeofbirth AS placeofbirth, \n" +
            "profile.gender AS gender, \n" +
            "profile.civilstatus AS civilstatus, \n" +
            "profile.res_Address_HouseNumber AS res_Address_HouseNumber, \n" +
            "profile.res_Address_Street AS res_Address_Street, \n" +
            "profile.res_Address_Barangay AS res_Address_Barangay, \n" +
            "profile.res_Address_Province AS res_Address_Province, \n" +
            "profile.res_Address_zipcode AS res_Address_zipcode, \n" +
            "profile.per_Address_HouseNumber AS per_Address_HouseNumber, \n" +
            "profile.per_Address_Street AS per_Address_Street, \n" +
            "profile.per_Address_Barangay AS per_Address_Barangay, \n" +
            "profile.per_Address_Province AS per_Address_Province, \n" +
            "profile.per_Address_zipcode AS per_Address_zipcode \n" +
            "FROM `tblaccount` AS acc\n" +
            "INNER JOIN `tblprofile` AS profile \n" +
            "ON acc.id = profile.profileID \n"+
            "WHERE acc.id = ?;";
            
        Connection con = null;
        try {
            
            con = DriverManager.getConnection(Database.dbURL, Database.dbUsername, Database.dbPassword);
            PreparedStatement checkStmt = con.prepareStatement(query);
            checkStmt.setInt(1, userID);
            ResultSet rs = checkStmt.executeQuery();
            
            while (rs.next()) {
                if(rs.getString("gender").equalsIgnoreCase("male"))
                    jrbtnGenderMale.setSelected(true);
                else if(rs.getString("gender").equalsIgnoreCase("female"))
                    jrbtnGenderFemale.setSelected(true);
                
                if(rs.getString("civilstatus").equalsIgnoreCase("single"))
                    jrbtnCVSingle.setSelected(true);
                else if(rs.getString("civilstatus").equalsIgnoreCase("married"))
                    jrbtnCVMarried.setSelected(true);
                else if(rs.getString("civilstatus").equalsIgnoreCase("divorced"))
                    jrbtnCVDivorced.setSelected(true);
                
                txtnationality.setText(rs.getString("nationality"));
                txtplaceOfBrith.setText(rs.getString("placeofbirth"));
                txtResAddr_HouseNumber.setText(rs.getString("res_Address_HouseNumber"));
                txtResAddr_Street.setText(rs.getString("res_Address_Street"));
                txtResAddr_Barangay.setText(rs.getString("res_Address_Barangay"));
                txtResAddr_Province.setText(rs.getString("res_Address_Province"));
                txtResAddr_zipcode.setText(rs.getString("res_Address_zipcode"));
                txtPermaAddr_HouseNumber.setText(rs.getString("per_Address_HouseNumber"));
                txtPermaAddr_Street.setText(rs.getString("per_Address_Street"));
                txtPermaAddr_Barangay.setText(rs.getString("per_Address_Barangay"));
                txtPermaAddr_Province.setText(rs.getString("per_Address_Province"));
                txtPermaAddr_zipcode.setText(rs.getString("per_Address_zipcode"));
                
            }
        
        } catch (SQLException ex) {
            System.out.println(ex);
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException closeEx) {
                    System.out.println(closeEx);
                }
            }
        }
    }
